public class Println {

    public static void main(String[] args) {
		
        System.out.print("Lest'go Java!"); 				     // Exibe na tela a mensagem
        System.out.println("New let's go Java!"); 		            // println -> Exibe na tela a mensagem com uma quebra de linha
	System.out.println("Welcome\nto\nJava\nProgramming!");             // Os \n fazem a quebra de linha
	System.out.printf("%s%n%s%n", "Welcome to", "Java Programming!"); // Impressão formatada "printf" - %n pulam linha
	        
	    //Atalho para saída -> Sout+Ctrl+Barra de espaço

	}
}
